    id INTEGER PRIMARY KEY,
    depname VARCHAR(30) NOT NULL,
    isocode CHAR(5) NOT NULL,
    zonesv_id INTEGER NOT NULL,
    FOREIGN KEY (zonesv_id) REFERENCES zonesv(id)
);

